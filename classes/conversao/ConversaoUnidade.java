package conversao;

public class ConversaoUnidade {
    public static double polegaParaCentimentro(double polegadas){
        return polegadas * 2.54;
    }
    public static double pesParaCentimentro(double pes){
        return pes * 30.48;
    }
    public static double milhasParaQuilometo(double milhas){
        return milhas * 1.609;
    }
}
